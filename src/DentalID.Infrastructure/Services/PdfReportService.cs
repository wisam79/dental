                            c.Item().Text("MATCH CONFIRMED").FontSize(24).Bold().FontColor(Colors.Green.Darken2);
                            c.Item().Text($"Confidence Score: {candidate.Score:P1}").FontSize(16).SemiBold();
                            c.Item().Text($"Method: {candidate.MatchMethod}").FontSize(12);
