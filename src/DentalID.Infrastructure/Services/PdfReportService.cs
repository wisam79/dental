                        {
                            c.Item().Text(subject.FullName).FontSize(20).SemiBold();
                            c.Item().Text($"Subject ID: {subject.SubjectId}").FontSize(14).FontColor(Colors.Grey.Darken2);
                            c.Item().Text($"National ID: {subject.NationalId ?? "N/A"}");
                            c.Item().Text($"DOB: {subject.DateOfBirth?.ToString("yyyy-MM-dd") ?? "Unknown"}");
                        });
                    });

                    // Dental DNA Code
                    column.Item().Border(1).BorderColor(Colors.Grey.Lighten2).Padding(10).Column(c => 
                    {
                        c.Item().Text("Latest Dental DNA Code").FontSize(14).SemiBold().FontColor(Colors.Blue.Medium);
                        c.Item().Text(subject.LatestDentalCode).FontFamily("Consolas").FontSize(16);
                    });

                    // Images Table
                    column.Item().Text("Dental Records").FontSize(16).SemiBold();
                    
                    column.Item().Table(table =>
                    {
                        table.ColumnsDefinition(columns =>
                        {
                            columns.ConstantColumn(100); // Date
                            columns.RelativeColumn();    // Fingerprint
                        });

                        table.Header(header =>
                        {
                            header.Cell().Element(CellStyle).Text("Date");
                            header.Cell().Element(CellStyle).Text("Fingerprint Code");

                            static IContainer CellStyle(IContainer container)
                            {
                                return container.DefaultTextStyle(x => x.SemiBold()).PaddingVertical(5).BorderBottom(1).BorderColor(Colors.Grey.Lighten2);
                            }
                        });

                        foreach (var img in subject.DentalImages)
                        {
                            table.Cell().Element(CellStyle).Text(img.UploadedAt.ToString("yyyy-MM-dd"));
                            table.Cell().Element(CellStyle).Text(img.FingerprintCode ?? "N/A").FontFamily("Consolas");
                            
