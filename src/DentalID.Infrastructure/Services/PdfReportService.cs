                page.Header().Element(header => ComposeHeader(header, "Positive Identification Report"));

                page.Content().PaddingVertical(1, Unit.Centimetre).Column(column =>
                {
                    column.Spacing(20);

                    // Match Verdict
                    column.Item().Background(Colors.Green.Lighten5).Border(1).BorderColor(Colors.Green.Medium).Padding(15).Row(row =>
                    {
                        row.RelativeItem().Column(c =>
