                    x.Span($" | Chain of Custody Hash: {custodyHash}");
                });
            });
        });

        return Task.FromResult(document.GeneratePdf());
    }

    private static string CalculateMatchCustodyHash(Subject probe, MatchCandidate candidate, DateTime generatedAtUtc)
    {
        var details = candidate.MatchDetails ?? new Dictionary<string, double>();
        var builder = new StringBuilder();
        builder.Append(probe.SubjectId ?? probe.FullName ?? "PROBE");
        builder.Append('|');
        builder.Append(candidate.Subject.SubjectId ?? candidate.Subject.FullName ?? "CANDIDATE");
        builder.Append('|');
        builder.Append(candidate.Score.ToString("F6", CultureInfo.InvariantCulture));
        builder.Append('|');
        builder.Append(candidate.MatchMethod ?? string.Empty);

        foreach (var detail in details.OrderBy(x => x.Key, StringComparer.Ordinal))
        {
            builder.Append('|');
            builder.Append(detail.Key);
            builder.Append(':');
            builder.Append(detail.Value.ToString("F6", CultureInfo.InvariantCulture));
        }

        builder.Append('|');
        builder.Append(generatedAtUtc.ToString("O", CultureInfo.InvariantCulture));

        using var sha256 = SHA256.Create();
        var hashBytes = sha256.ComputeHash(Encoding.UTF8.GetBytes(builder.ToString()));
        return Convert.ToHexString(hashBytes)[..12];
    }

